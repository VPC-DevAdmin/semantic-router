"""Generate the merged `config/vllm-sr.yaml` from per-tier YAMLs + routing template.

Inputs:
  - config/tiers/*.yaml         → source of truth for tier identity and backends
  - config/vllm-sr.routing.yaml → hand-maintained: listeners, signals, decisions,
                                  global, plus modelCards_overrides

Output:
  - config/vllm-sr.yaml         → merged, gitignored (it's a build artifact)

Generation rules:
  - `providers.defaults.default_model` = lowest-level tier name.
  - `providers.models[]` = one entry per tier:
      name              ← tier.router_alias
      provider_model_id ← tier.served_model_name
      api_format        ← "openai" (constant; only OAI-compat backends today)
      backend_refs[]    ← one per tier.router_backend_refs entry, with
                          a synthesized `name` ("backend0", ...), the
                          ref's `endpoint`, protocol "http", and weight.
  - `routing.modelCards[]` = one per tier, sorted by level; quality_score
    and description come from modelCards_overrides[tier.name] if present,
    else from level-derived defaults.

The generated file is the file the user passes to `vllm-sr serve --config`.
"""
from __future__ import annotations

from datetime import UTC, datetime
from pathlib import Path
from typing import Any

import yaml

from .config import ModelsConfig, TierConfig, load_models

DEFAULT_TIERS_DIR = Path("config/tiers")
DEFAULT_ROUTING_TEMPLATE = Path("config/vllm-sr.routing.yaml")
DEFAULT_OUTPUT = Path("config/vllm-sr.yaml")


def _tier_to_provider_model(tier: TierConfig) -> dict[str, Any]:
    refs = []
    for i, ref in enumerate(tier.router_backend_refs):
        refs.append(
            {
                "name": f"backend{i}",
                "endpoint": ref.endpoint,
                "protocol": "http",
                "weight": ref.weight,
            }
        )
    return {
        "name": tier.router_alias,
        "provider_model_id": tier.served_model_name,
        "api_format": "openai",
        "backend_refs": refs,
    }


def _tier_to_model_card(tier: TierConfig, overrides: dict[str, dict[str, Any]]) -> dict[str, Any]:
    o = overrides.get(tier.name, {})
    quality = o.get("quality_score", 0.50 + 0.10 * (tier.level - 1))
    description = o.get("description", f"Tier {tier.level} ({tier.name}).")
    return {
        "name": tier.router_alias,
        "modality": "text",
        "quality_score": quality,
        "description": description,
    }


def generate_router_config(
    *,
    tiers_dir: Path = DEFAULT_TIERS_DIR,
    routing_template: Path = DEFAULT_ROUTING_TEMPLATE,
    output: Path = DEFAULT_OUTPUT,
) -> Path:
    """Render the merged vllm-sr.yaml. Returns the output path."""
    models: ModelsConfig = load_models(tiers_dir)
    with routing_template.open("r", encoding="utf-8") as f:
        tpl: dict[str, Any] = yaml.safe_load(f) or {}

    overrides = tpl.pop("modelCards_overrides", None) or {}

    sorted_tiers = sorted(models.tiers, key=lambda t: t.level)
    if not sorted_tiers:
        raise ValueError("no tiers loaded; cannot generate router config")

    merged: dict[str, Any] = {}
    # Preserve template ordering on top-level keys; overlay our generated bits.
    merged["version"] = tpl.get("version", "v0.3")
    if "listeners" in tpl:
        merged["listeners"] = tpl["listeners"]

    merged["providers"] = {
        "defaults": {"default_model": sorted_tiers[0].router_alias},
        "models": [_tier_to_provider_model(t) for t in sorted_tiers],
    }

    routing = dict(tpl.get("routing") or {})
    cards = [_tier_to_model_card(t, overrides) for t in sorted_tiers]
    # Place modelCards first in the routing dict for readability.
    routing = {"modelCards": cards, **{k: v for k, v in routing.items() if k != "modelCards"}}
    merged["routing"] = routing

    if "global" in tpl:
        merged["global"] = tpl["global"]

    output.parent.mkdir(parents=True, exist_ok=True)
    header = (
        "# GENERATED by `benchmark gen-router-config` — do not edit by hand.\n"
        f"# Source: {tiers_dir}/*.yaml + {routing_template}\n"
        f"# Generated: {datetime.now(UTC).isoformat(timespec='seconds')}\n"
        "#\n"
        "# Edit per-tier identity/endpoint in config/tiers/*.yaml; edit routing\n"
        "# rules (signals, decisions, modelCards overrides) in\n"
        "# config/vllm-sr.routing.yaml. Then regenerate with `make gen-router-config`.\n"
        "\n"
    )
    body = yaml.safe_dump(merged, sort_keys=False, default_flow_style=False)
    output.write_text(header + body)
    return output
